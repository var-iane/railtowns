    <p>Welcome to the United States of America around 1850. It's young, overconfident, and growing too fast for its skin. People keep sailing over to find their version of the American Dream, and it's getting very crowded in those cities in the Northeast.</p>

    <p>Meanwhile, there's the whole West: so much land to take.</p>

    <p>The federal government finds a perfect partner in railroad companies. In exchange for millions of acres of free land on which to lay tracks, the railroads are going to help the United States settle the West.</p>

