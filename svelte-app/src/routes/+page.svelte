    <p>Railroad companies planned their routes, platted towns along them, and then promoted these towns aggressively to potential settlers. They did this at incredible speed.</p>

    <p>Some railroad towns became important cities. Many more remained small agricultural or mining towns, fully dependent on the lifeblood of the railroad (and the railroad dependent on the town in kind). And some disappeared as quickly as they came.</p>

